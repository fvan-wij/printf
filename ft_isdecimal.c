#include "libftprintf.h"

int ft_isdecimal(char *s, int i)
{   
    if((s[i] == '%' && s[i + 1] == 'd') || (s[i] == '%' && s[i + 1] == 'i') || (s[i] == '%' && s[i + 1] == 'u'))
        return (1);
    return (0);
}
